
// 1. Abs (Mutlak Değer)
int negatifSayi = -5;
Console.WriteLine("Abs: " + Math.Abs(negatifSayi)); // 5

// 2. Pow (Üssü Hesaplama)
double sayi = 2;
double sonuc = Math.Pow(sayi, 3); // 2^3
Console.WriteLine("Pow: " + sonuc); // 8

// 3. Sqrt (Karekök Alma)
double karekok = Math.Sqrt(16);
Console.WriteLine("Sqrt: " + karekok); // 4

// 4. Round (Yuvarlama)
double yuvarla = Math.Round(3.14159, 2);
Console.WriteLine("Round: " + yuvarla); // 3.14

// 5. Ceiling (Yukarıya Yuvarlama)
double yukariYuvarla = Math.Ceiling(3.1);
Console.WriteLine("Ceiling: " + yukariYuvarla); // 4

// 6. Floor (Aşağıya Yuvarlama)
double asagiYuvarla = Math.Floor(3.9);
Console.WriteLine("Floor: " + asagiYuvarla); // 3

// 7. Max (Büyük Değeri Seçme)
int maxDeger = Math.Max(10, 20);
Console.WriteLine("Max: " + maxDeger); // 20

// 8. Min (Küçük Değeri Seçme)
int minDeger = Math.Min(10, 20);
Console.WriteLine("Min: " + minDeger); // 10

// 9. Sin (Sinüs Hesaplama)
double sin = Math.Sin(Math.PI / 2); // PI/2'nin sinüsü
Console.WriteLine("Sin: " + sin); // 1

// 10. Cos (Kosinüs Hesaplama)
double cos = Math.Cos(Math.PI); // PI'nin kosinüsü
Console.WriteLine("Cos: " + cos); // -1

// 11. Tan (Tanjant Hesaplama)
double tan = Math.Tan(Math.PI / 4); // PI/4'ün tanjantı
Console.WriteLine("Tan: " + tan); // 1

// 12. Exp (e^x Hesaplama)
double exp = Math.Exp(1); // e^1
Console.WriteLine("Exp: " + exp); // 2.71828...

// 13. Log (Doğal Logaritma)
double log = Math.Log(10); // 10'un doğal logaritması
Console.WriteLine("Log: " + log); // 2.302585...

// 14. Log10 (Logaritma Base 10)
double log10 = Math.Log10(100); // 100'ün base 10 logaritması
Console.WriteLine("Log10: " + log10); // 2

// 15. PI (PI Sayısı)
Console.WriteLine("PI: " + Math.PI); // 3.14159265358979...

// 16. E (Euler Sayısı)
Console.WriteLine("E: " + Math.E); // 2.71828182845904...

